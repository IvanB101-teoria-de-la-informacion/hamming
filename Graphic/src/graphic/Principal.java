    public void PopUp(String Imprimir, String imagen, String titulo) {
        Icon ip = new javax.swing.ImageIcon(getClass().getResource(imagen));

        JLabel jLabel9 = new javax.swing.JLabel();
        jLabel9.setFont(new java.awt.Font("Microsoft YaHei", 1, 17));
        jLabel9.setText(Imprimir);

        JOptionPane.showMessageDialog(this, jLabel9,
            " "+ titulo, JOptionPane.PLAIN_MESSAGE, ip);
    }

